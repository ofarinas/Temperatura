    private static String getGrade(String respuestaTemperaturaServicio, String aux) {
        return getTemperatura(respuestaTemperaturaServicio, aux).substring(getTemperatura(respuestaTemperaturaServicio, aux).indexOf("("));
    }

    private static String getTemperatura(String respuestaTemperaturaServicio, String aux) {
        return respuestaTemperaturaServicio.substring(getIndiceTemperatura1(respuestaTemperaturaServicio, aux) + aux.length(), getIndiceTemperatura2(respuestaTemperaturaServicio));
    }

    private static int getIndiceTemperatura2(String respuestaTemperaturaServicio) {
        return respuestaTemperaturaServicio.indexOf("</Temperature>");
    }

    private static int getIndiceTemperatura1(String respuestaTemperaturaServicio, String aux) {
        return respuestaTemperaturaServicio.lastIndexOf(aux);
    }

