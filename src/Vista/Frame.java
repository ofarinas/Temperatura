    private List getCity(String respuestaServicio) {
        String[] citis = respuestaServicio.split("<City>");
        List listaciudades = new ArrayList();
        for (int i = 0; i < citis.length; i++) {
            int index = citis[i].indexOf("</City>");
            if (index > 0) {
                listaciudades.add(citis[i].substring(0, index));
            }

        }
        return listaciudades;
    }
